package day4;

public class Math_class {

		public static void main(String[] args) {
			System.out.println("abs value of -7 : " + Math.abs(-7));
			System.out.println("3 power 4 : " + Math.pow(3, 4));
			System.out.println("Sqrt of 81 : " + Math.sqrt(81));
			System.out.println("Ramdon value between 0 to 1 : " + Math.random());
			System.out.println("Min value : " + Math.min(100, 50));
			System.out.println("Max value : " + Math.max(60, 88));
			
			System.out.println("Round(3.6) : " + Math.round(3.6));
			System.out.println("Floor(3.6) : " + Math.floor(3.6));
			System.out.println("Ceil(3.2) : " + Math.ceil(3.2));
			
			System.out.println("Sin(30) : " + Math.sin(30));
			System.out.println("Log10(10) : " + Math.log(10));
		}
}
